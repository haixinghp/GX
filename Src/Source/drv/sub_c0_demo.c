const unsigned int C0_into_poweroff2_total_words = 191;

void Sub_C0_into_poweroff2()
{
		int i = 0;
	
	CPM->CPM_CHIPCFGR |= (1 << 19);   //enable sub system to main system output isolation
	CPM->CPM_SYSRSTCR = (1<<22);  		//reset M2S_BUS
	delay(1500);											//delay MUST more than 10us.	
	CPM->CPM_CHIPCFGR &=~ (1 << 19);	//disable sub system to main system output isolation
	CPM->CPM_SYSRSTCR = 0;
	delay(1500);					//delay MUST more than 10us.	

#ifdef C0_REGISTER_TEST
	C0_CPM->CPM_VCCQGTRIMR |= (1 << 30);  //ldo enter lowpower in normal mode, See VDD_SUB 0.9V 
	
  C0_WDT->WDT_WCR = 0x0; //Close C0 WDT
  C0_TC->TCCR = 0x0; //Close C0 TC
#endif

//#ifdef C0_POFF2_TEST
  C0_load_bin_to_mem((UINT8 *)C0_into_poweroff2, C0_into_poweroff2_total_words*4);//WK_SUB PAD wakeup, Flash LED(EPORT8)
	C0_CPM->CPM_SRSTMASKR &= ~(0x80000000); //assert the reset of Core CS0.
//#endif

	delay(30000);	//delay MUST more than 10us.

	C0_SLPCR = 0x1;

	delay(30);	//delay MUST more than 10us.
	
	CPM->CPM_CHIPCFGR |= (1 << 19);   //enable sub system to main system output isolation
}
