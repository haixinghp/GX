/***************************************************************************************
**函数名:       BasicFunc_GetSumChk
**功能描述:     获取校验和
**输入参数:     
uint8_t *dat, 数据
uint16_t len 长度
**输出参数:     
uint8_t
结果
**备注:         
****************************************************************************************/
uint16_t BasicFunc_GetSumChk(uint8_t *dat, uint16_t len)
{
    uint16_t i;
    uint16_t chkTmp = NULL;
    if (NULL == dat)
    {
        return 0; //错误
    }
    chkTmp = *dat;
    for (i = 1; i < len; i++)
    {
        if (NULL == (dat + i))
        {
            return 0; //错误
        }
        chkTmp += *(dat + i);
    }
    return chkTmp;
}
